(x1,x2,x3,x4) = (0,1,0,2)
(y1,y2,y3,y4) = (0,0,1,2)

ex = [(α) -> x1 * (1 .- α) / 2 + x3 * (1 .+ α) / 2,
(α) -> x2 * (1 .- α) / 2 + x4 * (1 .+ α) / 2,
(α) -> x1 * (1 .- α) / 2 + x2 * (1 .+ α) / 2,
(α) -> x3 * (1 .- α) / 2 + x4 * (1 .+ α) / 2]

exα = [(α) -> -x1 / 2 + x3 / 2,
 (α) -> -x2 / 2 + x4 / 2,
 (α) -> -x1 / 2 + x2 / 2,
 (α) -> -x3 / 2 + x4 / 2]

ey = [(α) -> y1 * (1 .- α) / 2 + y3 * (1 .+ α) / 2,
(α) -> y2 * (1 .- α) / 2 + y4 * (1 .+ α) / 2,
(α) -> y1 * (1 .- α) / 2 + y2 * (1 .+ α) / 2,
(α) -> y3 * (1 .- α) / 2 + y4 * (1 .+ α) / 2]

eyα = [(α) -> -y1 / 2 + y3 / 2,
 (α) -> -y2 / 2 + y4 / 2,
 (α) -> -y1 / 2 + y2 / 2,
 (α) -> -y3 / 2 + y4 / 2]

 
xt(r,s) = transfinite_blend(ex[1], ex[2], ex[3], ex[4],
 exα[1], exα[2], exα[3], exα[4],
 r, s)

yt(r,s) = transfinite_blend(ey[1], ey[2], ey[3], ey[4],
 eyα[1], eyα[2], eyα[3], eyα[4],
 r, s)